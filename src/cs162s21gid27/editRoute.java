    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        UsersCrud crud = UsersCrud.getInstance();
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Enter Bus Route No");
        } else {
            routeDetail route = new routeDetail();
            route.setBusNo(jTextField1.getText());
            if (crud.searchRoute(route) != -1) {
                jTextField2.setText(crud.getrouteList().get(crud.searchRoute(route)).getBusNo());
                jTextField3.setText(crud.getrouteList().get(crud.searchRoute(route)).getArrCity());
                jTextField4.setText(crud.getrouteList().get(crud.searchRoute(route)).getDepCity());
                jTabbedPane1.setSelectedIndex(1);
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Credentials");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

