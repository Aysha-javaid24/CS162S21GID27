    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        UsersCrud crud = UsersCrud.getInstance();
        routeDetail route = new routeDetail();
        route.setBusNo(jTextField1.getText());
        int index = crud.searchRoute(route);
        route.setDepCity(jTextField4.getText());
        route.setArrCity(jTextField3.getText());
        route.setBusNo(jTextField2.getText());
        if (Validators.validateName(jTextField4.getText()) == true && Validators.validateName(jTextField3.getText()) == true && Validators.validateBusNo(jTextField2.getText())) {
            crud.editRoute(index, route);
            JOptionPane.showMessageDialog(null, "Route data is Updated Successfully");
            manageRoutes mange = new manageRoutes();
            crud.setUserList(crud.getUserList());
            saveRoutes();
            mange.setVisible(true);
            this.setVisible(false);
        } else {
             JOptionPane.showMessageDialog(null, "Invalid Data");
        }
    }//GEN-LAST:event_jButton4ActionPerformed
    private void saveRoutes() {
        try {
            UsersCrud crud = UsersCrud.getInstance();
            FileWriter writer = new FileWriter("Routes.txt");
            writer.write("BusNo:ArrivalCity:DepartureCity");
            for (int i = 0; i < crud.getrouteList().size(); i++) {
                writer.write("\n"+crud.getrouteList().get(i).getBusNo() + ":");
                writer.write(crud.getrouteList().get(i).getArrCity() + ":");
                writer.write(crud.getrouteList().get(i).getDepCity());
            }
            writer.close();

        } catch (IOException ex) {
             JOptionPane.showMessageDialog(null, "Error....");
        }
    }
